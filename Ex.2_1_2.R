# Rounding 
# floor(x) rounds to the nearest integer that's smaller than x
floor(2.8)
#ceiling(x) rounds to the nearest integer that's larger than x
ceiling(3.7)
# use with negative numbers 
ceiling(-4.7)
floor(-4.7)
#trunc(x) rounds to the nearest integer in the direction of 0
trunc(2.8)
trunc(-2.8)
# round function round upto specified digit mentioned in argument.
round(23.456789,4)
#signif rounds the values in its first argument to the specified number of significant digits.
signif(-23.123456,4)